    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int valueA = side1.getValue();
        int valueB = side2.getValue();
        int valueC = side3.getValue();
        
        if (valueA == valueB && valueB == valueC) {
            res1.setText("Pode formar um triangulo");
            res2.setText("Triangulo Equilatero");
        } else {
            if (valueA == valueB || valueA == valueC || valueB == valueC) {
                res1.setText("Pode formar um triangulo");
                res2.setText("Triangulo Isoceles");
            } else {
                if (valueA == 0 || valueB == 0 || valueC == 0) {
                    res1.setText("NÃ£o pode formar um triangulo");
                    res2.setText("-");
                } else {
                    res1.setText("Pode formar um triangulo");
                    res2.setText("Triangulo Escaleno");
                }
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

